<h1>The Greenest Cup Ever.</h1> 

<h2>The EarthCup is a beautiful, functional, reusable take-away cup hand-crafted from bamboo and teak wood.  Not only does it carry your favorite beverage in epic green styleâ€” it removes CO2 and plastic from the biosphere.</h2><br>

<div id="image"><img src="/images/virtue-icons.svg?v=3.1" width="80%" alt="Regenerative Virtue Icons"></div>
<p>Moving on from petro-plastics and industrial production, the EarthCup is the ultimate green drinking cup.  Made for-Earth, rather than for-profit, made by hand rather than by factory, made from bamboo and wood rather than industrial materials, the EarthCup takes green to a whole new level.</p>
<p>Unlike normal disposal cups and reusable tumblers whose manufacture results in plastic being made and carbon being emitted, the generation of an EarthCup results in just the opposite.  By sequestering carbon in its biomass and taking the place of polluting alternatives, the EarthCup is a net-green regenerative product.</p> 

  
